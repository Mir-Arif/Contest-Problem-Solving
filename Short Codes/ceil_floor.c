#include<stdio.h>
#include<math.h>
int main(void)
{
printf("\nCosine of 0 is: %f\n", cos(0));
printf("\nCeiling of 0.001 is: %f\n", ceil(0.001));
printf("\nFloor of 2.999 is: %f\n", floor(2.999));
printf("\n0.1 raised to the power 3 is: %f\n", pow(0.1, 3));
printf("\nValue of e raised to the power 3 is: %f\n", exp(3));
printf("\nValue of natural logarithm of 6 is: %f\n", log(6));
printf("\nValue of decimal logarithm of 1000 is: %f\n", log10(1000));
printf("\nValue of square root of 6 is: %f\n", sqrt(6));
printf("\nAbsolute value of -5.3 is: %f\n", fabs(-5.3));
return 0;
}
